//while  - teste no inicio
print("usando o while")
var i = 1
while i <= 10 {
    print(i)
    i += 1
}

//repeat ou do while - faz uma instrucao e depois checa a condicao
print("usando o repeat")
var a = 1
repeat {
    print(a)
    a += 1
} while a <= 10
